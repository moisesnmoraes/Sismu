    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $categorias = Categoria::select('descricao', 'id')->pluck('descricao', 'id');
        return view('produto.formulario', compact('categorias'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $produto = new Produto();
        $produto->fill($request->all());
        $produto->save();
        $request->session()->flash('mensagem_sucesso', 'Produto salvo com sucesso!');
        return Redirect::to('produto/create');
    }

